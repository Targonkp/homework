export function Coffeemaker(coffee, sum) {


   if (coffee=='cofee3in1'){
       return sum-30;
   }
   else
       if (coffee=='blackcofee')
       {
           return sum-35;
       }
     else
       if (coffee=='cofeewithcream')
       {
           return sum-40;
       }
       else
       if (coffee=='latte')
       {
           return sum-45;
       }
}